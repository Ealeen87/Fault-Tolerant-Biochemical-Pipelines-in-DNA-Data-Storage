# GLOBAL SYSTEM PARAMETERS & CONSTRAINT CONFIGURATION
BASES = ['A', 'C', 'G', 'T']
BASE_MAP = {(0, 0): 'A', (0, 1): 'C', (1, 0): 'G', (1, 1): 'T'}

# Section 4.1 Parameter Bounds
GC_MIN = 0.45
GC_MAX = 0.55
HOMO_MAX = 3
LOOKAHEAD = 4

# DATA STRUCTURE CORE UTILITIES
def text_to_bits(text):
    """Converts a standard text string into a flat list of binary integers."""
    bits = []
    for char in text:
        bits.extend([int(b) for b in format(ord(char), '08b')])
    return bits

def bits_to_text(bits):
    """Converts a flat list of binary integers back into a text string."""
    chars = []
    for i in range(0, len(bits), 8):
        byte = bits[i:i+8]
        if len(byte) < 8:
            break
        chars.append(chr(int(''.join(map(str, byte)), 2)))
    return ''.join(chars)

def xor_bits(bitstream_a, bitstream_b):
    """Executes an element-wise bitwise Exclusive-OR (XOR) logic gate operation."""
    return [a ^ b for a, b in zip(bitstream_a, bitstream_b)]

def calculate_ber(original_bits, recovered_bits, decoding_failed=False):
    """Calculates the exact Bit Error Rate (BER) between two bit arrays."""
    if decoding_failed:
        return 1.0
    if not original_bits or not recovered_bits:
        return 1.0
    total_length = max(len(original_bits), len(recovered_bits))
    min_length = min(len(original_bits), len(recovered_bits))

    errors = sum(1 for i in range(min_length) if original_bits[i] != recovered_bits[i])
    errors += abs(len(original_bits) - len(recovered_bits))
    return errors / total_length

# ALGORITHM 4.1: LOOK-AHEAD CONSTRAINT SOLVER
def check_homopolymer(sequence, candidate_base):
    """Evaluates if appending the candidate base breaches the homopolymer threshold."""
    if len(sequence) < HOMO_MAX - 1:
        return False
    return all(base == candidate_base for base in sequence[-(HOMO_MAX - 1):])

def check_gc_content(sequence, candidate_base):
    """Evaluates if appending the candidate base violates GC balance boundaries."""
    test_sequence = sequence + candidate_base
    if len(test_sequence) < 10:
        return False
    gc_count = sum(1 for base in test_sequence if base in ('G', 'C'))
    gc_ratio = gc_count / len(test_sequence)
    return not (GC_MIN <= gc_ratio <= GC_MAX)

def apply_dynamic_remap(candidate_base, sequence):
    """Applies dynamic mathematical re-mapping rule to guarantee sequence stability."""
    alternative_bases = [base for base in BASES if base != candidate_base]
    random.shuffle(alternative_bases)

    for alt in alternative_bases:
        if not check_homopolymer(sequence, alt) and not check_gc_content(sequence, alt):
            return alt

    for alt in alternative_bases:
        if not check_homopolymer(sequence, alt):
            return alt

    return alternative_bases[0]

def look_ahead_constraint_solver(bits):
    """
    Input: Binary Bitstream (B)
    Output: Compliant DNA Sequence String (S)
    """
    sequence = ''
    i = 0
    while i < len(bits) - 1:
        pair = (bits[i], bits[i+1])
        candidate = BASE_MAP.get(pair, 'A')

        future_violation = False
        temp_seq = sequence + candidate
        for j in range(1, LOOKAHEAD + 1):
            if i + 2*j + 1 >= len(bits):
                break
            future_pair = (bits[i + 2*j], bits[i + 2*j + 1])
            future_base = BASE_MAP.get(future_pair, 'A')
            if check_homopolymer(temp_seq, future_base):
                future_violation = True
                break
            temp_seq += future_base

        if check_homopolymer(sequence, candidate) or \
           check_gc_content(sequence, candidate) or \
           future_violation:
            candidate = apply_dynamic_remap(candidate, sequence)

        sequence += candidate
        i += 2
    return sequence

# SECTION 4.2: DISTRIBUTED FOUNTAIN CODE & PARITY SHIELD
def fountain_encode(bits, strand_size=32, expansion_factor=1.5):
    """Partitions a file bitstream into an elastic stream of overlapping packages."""
    chunks = []
    for i in range(0, len(bits), strand_size):
        chunk = bits[i:i+strand_size]
        while len(chunk) < strand_size:
            chunk.append(0)
        chunks.append(chunk)

    num_chunks = len(chunks)
    strands = []

    for idx, chunk in enumerate(chunks):
        local_parity = sum(chunk) % 2
        strands.append(([idx], chunk[:], local_parity))

    num_redundant_strands = int(num_chunks * expansion_factor)
    for _ in range(num_redundant_strands):
        max_degree = min(5, num_chunks)
        degree = random.randint(2, max_degree) if max_degree >= 2 else 1
        indices = random.sample(range(num_chunks), degree)

        encoded_payload = chunks[indices[0]][:]
        for idx in indices[1:]:
            encoded_payload = xor_bits(encoded_payload, chunks[idx])

        local_parity = sum(encoded_payload) % 2
        strands.append((indices, encoded_payload, local_parity))

    random.shuffle(strands)
    return strands, num_chunks, strand_size

def fountain_decode(strands, num_chunks, strand_size):
    """Belief-Propagation Iterative Peeling Decoder utilizing localized XOR gates."""
    recovered_map = {}
    remaining_pool = []

    for (indices, encoded, parity) in strands:
        if sum(encoded) % 2 != parity:
            continue
        if len(indices) == 1:
            if indices[0] not in recovered_map:
                recovered_map[indices[0]] = encoded[:]
        else:
            remaining_pool.append((list(indices), encoded[:]))

    execution_state_changed = True
    while execution_state_changed:
        execution_state_changed = False
        deferred_pool = []

        for (indices, encoded) in remaining_pool:
            known_indices = [idx for idx in indices if idx in recovered_map]
            unknown_indices = [idx for idx in indices if idx not in recovered_map]

            current_payload = encoded[:]
            for idx in known_indices:
                current_payload = xor_bits(current_payload, recovered_map[idx])

            if len(unknown_indices) == 1:
                recovered_map[unknown_indices[0]] = current_payload[:]
                execution_state_changed = True
            elif len(unknown_indices) > 1:
                deferred_pool.append((unknown_indices, current_payload))

        remaining_pool = deferred_pool

    # Track if major sections of the file were dropped permanently
    unrecovered_count = sum(1 for i in range(num_chunks) if i not in recovered_map)
    decoding_failed = unrecovered_count > (num_chunks * 0.05)

    final_reconstruction = []
    for i in range(num_chunks):
        if i in recovered_map:
            final_reconstruction.extend(recovered_map[i])
        else:
            final_reconstruction.extend([0] * strand_size)

    return final_reconstruction, decoding_failed

# SECTION 4.3: SIMULATION ENGINES AND CHANNEL NOISE MODELS
def apply_chemical_erasure_channel(strands, corruption_rate):
    """Simulates chemical degradation leading to random dropouts and missing strands."""
    processed_strands = []
    for (indices, encoded, parity) in strands:
        # If random value hits corruption threshold, the strand is completely erased (dropped)
        if random.random() >= corruption_rate:
            processed_strands.append((indices, encoded, parity))
    return processed_strands

def execute_pipeline_trial(payload_text, corruption_rate):
    """Executes a single end-to-end evaluation iteration across the pipeline layers."""
    bitstream = text_to_bits(payload_text)

    dna_sequence = look_ahead_constraint_solver(bitstream)
    homopolymer_violations = sum(
        1 for i in range(len(dna_sequence) - HOMO_MAX + 1)
        if len(set(dna_sequence[i:i+HOMO_MAX])) == 1
    )

    strands, num_chunks, strand_size = fountain_encode(bitstream, strand_size=32, expansion_factor=1.5)
    noisy_strands = apply_chemical_erasure_channel(strands, corruption_rate)

    decoded_bits, decoding_failed = fountain_decode(noisy_strands, num_chunks, strand_size)
    decoded_bits = decoded_bits[:len(bitstream)]

    calculated_ber = calculate_ber(bitstream, decoded_bits, decoding_failed)
    return calculated_ber, homopolymer_violations

